const dictionaries: Record<Locale, Record<TranslationKey, string>> = {
  "es-MX": esMX,
  "en-US": enUS,
  "zh-CN": zhCN,
};

export function isLocale(value: string | null | undefined): value is Locale {
  return locales.includes(value as Locale);
}

export function normalizeLocale(value: string | null | undefined): Locale {
  if (!value) return defaultLocale;
  if (isLocale(value)) return value;
  const lower = value.toLowerCase();
  if (lower.startsWith("es")) return "es-MX";
  if (lower.startsWith("zh")) return "zh-CN";
  return "en-US";
}

export function t(locale: Locale, key: TranslationKey, params: Record<string, string | number> = {}) {
  const template = dictionaries[locale][key] ?? dictionaries[defaultLocale][key] ?? key;
  return Object.entries(params).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}

export function getStoredLocale() {
  if (typeof localStorage === "undefined") return defaultLocale;
  return normalizeLocale(localStorage.getItem(localeStorageKey));
}

export function detectBrowserLocale() {
  if (typeof navigator === "undefined") return defaultLocale;
  return normalizeLocale(navigator.languages?.[0] ?? navigator.language);
}

export function getInitialLocale() {
  if (typeof localStorage !== "undefined") {
    const stored = localStorage.getItem(localeStorageKey);
    if (stored) return normalizeLocale(stored);
  }
  return detectBrowserLocale();
}

export function persistLocale(locale: Locale) {
  if (typeof localStorage !== "undefined") localStorage.setItem(localeStorageKey, locale);
}
